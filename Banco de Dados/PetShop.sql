CREATE DATABASE PETSHOP;
USE PETSHOP;

CREATE TABLE CLIENTE(CPF varchar(11), NOME varchar(100),
primary key(CPF));

CREATE TABLE LOJA(TICKET INT, DATA_COMPRA DATE, DESCRICAO varchar(50),
QUANTIDADE INT, PRECO decimal(6, 2), CPF_FK varchar(11),
foreign key(CPF_FK) references CLIENTE(CPF) ,primary key(TICKET));

CREATE TABLE ANIMAL (CODIGO INT,
 NOME varchar(50),
 RACA varchar(30),
 ESPECIE varchar(30),
 SEXO varchar(1),
 DATA_NASCIMENTO DATE,
 CPF_FK varchar(11),
 foreign key(CPF_FK) references CLIENTE(CPF),
 primary key (CODIGO));
 
CREATE TABLE CONSULTA (CODIGO INT,
 PRECO decimal(6, 2),
 DESCRICAO varchar(50),
 CRMV varchar(11),
 DATA_ATENDIMENTO DATE,
 CODIGO_FK INT,
 foreign key(CODIGO_FK) references ANIMAL(CODIGO),
 primary key (CODIGO));
 
CREATE TABLE VACINA (CODIGO INT,
 PRECO decimal(6, 2),
 DATA_APLICACAO DATE,
 LOTE varchar(11),
 DOENCA varchar(30),
 CODIGO_FK INT,
 foreign key(CODIGO_FK) references ANIMAL(CODIGO),
 primary key (CODIGO));

